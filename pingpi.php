<?php
$money = 1200; // จำนวนเงินของนายสมชาย

if ($money < 500) {
    echo "นายสมชายสามารถซื้อกางเกงได้";
} elseif ($money < 1000) {
    echo "นายสมชายสามารถซื้อกางเกงและเสื้อได้";
} elseif ($money < 1500) {
    echo "นายสมชายสามารถซื้อกางเกง เสื้อ และรองเท้าได้";
} else {
    echo "นายสมชายมีเงินมากกว่า หรือเท่ากับ 1,500 บาท";
}
?>
