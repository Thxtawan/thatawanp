<?php
    $A = 'B';   // ตัวแปร $A มีค่าเท่ากับตัวอักษร 'B'
    $$A = 20.5; // $$A จะหมายถึงสร้างตัวแปรใหม่ชื่อ $B (ตามค่าของ $A) ให้มีค่า 20.5

    echo "\$A=$A <br>";
    echo "\$\$A=" . $$A . "<br>";
    echo "\$B=$B <br>"; // เมื่อพิมพ์ $B ออกมา ค่าจึงเท่ากับ 20.5 ด้วยนั่นเอง
?>
