<?php
    $a = 3;
    $b = 4;
    $sum = $a + $b; // นำตัวแปรมาบวกกัน

    // ใส่ \ หน้าเครื่องหมาย $ เพื่อให้โปรแกรมพิมพ์คำว่า $a ออกมาเป็นข้อความ ไม่ใช่ดึงค่าตัวแปร
    echo "ค่าของ \$a คือ $a <br>";
    echo "ค่าของ \$b คือ $b <br>";
    echo "ผลรวมของ \$a และ \$b คือ $sum <br>";
?>

